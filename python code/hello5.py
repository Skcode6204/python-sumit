#string slicing

mystr = "my name is sumit"  
print(mystr[0:7])           #here it slices the all words except 0-7th position.

mystr = "my age is 22"
print(mystr[1:12:6])  #here it slices the other alphabets except 1th and 6th position

mystr = "my father name is sonu kumar"
print(mystr[::])

mystr = "i live in dhanbad"
print(mystr[::2])

mystr = "master in dealing school uniform"
print(mystr[-4:-2])

mystr = "master in dealing school uniform"
print(mystr[28:30])

mystr = "sumit is the good boy"
print(mystr[::-2])